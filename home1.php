<center><h2 style="color:red">INTRODUCTION</h2></center>
<center><p style="color:white">
The Student Attendance Management System is a software developed for daily attendance of students in colleges. It facilitates the access of attendance information of a particular student belongs to particular class.The attendance information is provided to the teacher for a particular class and subject. 
The software system also hels in evaluating the examination criteria for a student in the sense that only those students with attendance above 75% are allowed to be eligible for a semester exam.

</p></center>
<center><h2 style="color:red">PURPOSE</h2></center>
<center><p style="color:white">
The purpose of using attendance management system is to computerize the traditional way of taking attendance and maintaining the register and generating the reports automatically whenever required-in between the semester or at the end of the semester.
This project is basically a desktop application which means self contained software runs on the system on which it has been installed under the user control and it will work for a particular institute or college only.

</p></center>

